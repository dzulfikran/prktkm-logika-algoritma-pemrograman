#include <iostream>

using namespace std;

int main() {
	int nilai1 = 7, nilai2 = 2;
	int hasil;
	hasil = nilai1 + nilai2;
	cout<<"Hasil "<<nilai1<<" + "<<nilai2<<" = "<<hasil<<endl;
	hasil = nilai1 - nilai2;
	cout<<"Hasil "<<nilai1<<" - "<<nilai2<<" = "<<hasil<<endl;
	hasil = nilai1 * nilai2;
	cout<<"Hasil "<<nilai1<<" * "<<nilai2<<" = "<<hasil<<endl;
	hasil = nilai1 / nilai2;
	cout<<"Hasil "<<nilai1<<" / "<<nilai2<<" = "<<hasil<<endl;
	hasil = nilai1 % nilai2;
	cout<<"Hasil "<<nilai1<<" MODULUS "<<nilai2<<" = "<<hasil<<endl;
	
	return 0;
}
